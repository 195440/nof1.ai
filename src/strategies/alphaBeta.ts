import type { StrategyParams, StrategyPromptContext } from "./types";

/**
 * Alpha Beta 策略配置 v4.0
 *
 * 核心设计理念：
 * - AI作为独立思考的交易员，综合技术面+消息面决策
 * - 按配置周期检查，兼容 5 分钟运行，关注 1H/4H 趋势
 * - 分析框架辅助思考，不是机械评分门槛
 * - 消息面可一票否决技术面
 * - 硬性约束只保留真正的风控底线（止损、仓位、频率）
 *
 * v4.0 重构内容：
 * - 移除机械评分系统，改为6维度分析框架
 * - 加入消息面/情绪面作为核心决策因素
 * - AI独立判断是否开仓，而非分数>=70就开
 * - 强调"宁可错过不可做错"
 *
 * @param maxLeverage - 系统允许的最大杠杆倍数
 * @returns Alpha Beta 策略的完整参数配置
 */
export function getAlphaBetaStrategy(maxLeverage: number): StrategyParams {
  const alphaBetaLevMin = Math.min(maxLeverage, 8);
  const alphaBetaLevMax = Math.min(maxLeverage, 12);
  const alphaBetaStrongMin = Math.min(alphaBetaLevMax, alphaBetaLevMin + 2);

  return {
    // ==================== 策略基本信息 ====================
    name: "Alpha Beta",
    description: "AI独立决策，按配置周期运行，技术面+消息面综合判断，震荡市少做、盈利更早落袋",
    
    // ==================== 杠杆配置 ====================
    leverageMin: alphaBetaLevMin,
    leverageMax: alphaBetaLevMax,
    leverageRecommend: {
      normal: `${alphaBetaLevMin}倍（普通机会，默认起步杠杆）`,
      good: `${alphaBetaLevMin + 1}-${Math.min(alphaBetaLevMax, alphaBetaLevMin + 2)}倍（确认充分的高质量机会）`,
      strong: `${alphaBetaStrongMin}-${alphaBetaLevMax}倍（极强信号，谨慎使用）`,
    },
    
    // ==================== 仓位配置 ====================
    positionSizeMin: 20,
    positionSizeMax: 25,
    maxTotalMarginPercent: 35,
    positionSizeRecommend: {
      normal: "20-21%（普通机会，默认起步仓位）",
      good: "22-23%（确认充分的高质量机会）",
      strong: "20-25%（多维共振的强机会）",
    },
    
    // ==================== 止损配置 ====================
    stopLoss: {
      low: -4,    // 低杠杆：扩大一点容错，减少震荡洗出
      mid: -4.2,  // 中杠杆：略宽于原配置，避免反复横跳
      high: -4.5, // 高杠杆：在降低杠杆上限的前提下给足波动空间
    },
    
    // ==================== 移动止盈配置（更早锁利）====================
    trailingStop: {
      level1: { trigger: 2.5, stopAt: 2 },   // 盈利2.5%时，至少锁定覆盖手续费后的净利润
      level2: { trigger: 4.5, stopAt: 3.2 }, // 盈利4.5%时，回吐空间继续压缩
      level3: { trigger: 7.5, stopAt: 5.5 }, // 盈利7.5%时，优先保护大部分利润
    },
    
    // ==================== 分批止盈配置（更果断落袋）====================
    partialTakeProfit: {
      stage1: { trigger: 3.5, closePercent: 60 }, // 盈利3.5%时，先锁定大部分利润
      stage2: { trigger: 7, closePercent: 85 },   // 盈利7%时，只保留小尾仓
      stage3: { trigger: 10, closePercent: 100 }, // 盈利10%时，全部落袋
    },
    
    // ==================== 峰值回撤保护 ====================
    peakDrawdownProtection: 25,
    
    // ==================== 波动率调整 ====================
    volatilityAdjustment: {
      highVolatility: { leverageFactor: 0.8, positionFactor: 0.75 },
      normalVolatility: { leverageFactor: 1.0, positionFactor: 1.0 },
      lowVolatility: { leverageFactor: 1.0, positionFactor: 1.05 },
    },
    
    // ==================== 策略规则描述 ====================
    entryCondition: "技术面+消息面综合判断，顺势优先，震荡市只做高质量机会，反转单必须说明结构依据与失效条件",
    riskTolerance: "扩大一点单笔容错，保持8倍起步杠杆并提高起步仓位到20%，盈利优先落袋，最大单笔仓位25%，总保证金≤35%，同币种平仓后至少等待3个周期",
    tradingStyle: "5分钟复盘，偏高频但不乱开仓；震荡市少出手，盈利达到2.5-4%优先锁利，避免浮盈回吐",
    
    // ==================== 代码级保护开关 ====================
    enableCodeLevelProtection: true,
    allowAiOverrideProtection: true,
    
    // ==================== 最大空仓时间限制 ====================
    maxIdleHours: 24,  // 延长到24小时，避免在没有好机会时被强迫开仓
  };
}

